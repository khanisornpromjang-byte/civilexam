// เพิ่ม Event Listener สำหรับการปรับขนาดหน้าจอ เพื่อจัดการปุ่ม openbtn
window.addEventListener('resize', function() {
    if (window.innerWidth > 768) { // ถ้าหน้าจอใหญ่กว่า 768px
        document.querySelector('.openbtn').style.display = 'block'; // แสดงปุ่ม openbtn เสมอ
        document.getElementById("mySidebar").style.width = "0"; // ปิด sidebar อัตโนมัติ
        document.getElementById("main").style.marginLeft = "0"; // เลื่อนเนื้อหาหลักกลับ
    } else { // ถ้าหน้าจอเล็กกว่าหรือเท่ากับ 768px
        if (document.getElementById("mySidebar").style.width === "0px") { // ถ้า sidebar ปิดอยู่
            document.querySelector('.openbtn').style.display = 'block'; // แสดงปุ่ม openbtn
        }
    }
});
