// ฟังก์ชันสำหรับแสดงคำถาม
function showQuestion(index) {
    // ตรวจสอบว่า index ไม่เกินจำนวนคำถามที่มี
    if (index >= questions.length) {
        displayResult();
        return;
    }

    const questionData = questions[index];
    // ลบตัวเลขนำหน้าออกจาก questionData.question ก่อนแสดงผล
    const cleanQuestionText = questionData.question.replace(/^\d+\.\s*/, '');
    document.getElementById('questionText').innerText = (index + 1) + ". " + cleanQuestionText;
    
    const optionsContainer = document.getElementById('optionsContainer');
    optionsContainer.innerHTML = ''; // ล้างตัวเลือกเก่าออก
    optionsContainer.style.pointerEvents = 'auto'; // เปิดใช้งานการคลิก

    // ซ่อนส่วน feedback และปุ่ม "ข้อต่อไป" ทุกครั้งที่แสดงข้อใหม่
    document.getElementById('feedbackContainer').style.display = 'none';
    document.getElementById('nextButton').style.display = 'none';

    questionData.options.forEach((option, i) => {
        const button = document.createElement('button');
        button.innerText = option;
        button.classList.add('option-button');
        
        button.addEventListener('click', () => {
            if (selectedOptionButton === null) { // ตรวจสอบว่ายังไม่ได้เลือกคำตอบ
                selectedOptionButton = button; // เก็บปุ่มที่ถูกเลือก
                checkAnswer(i, questionData.answer, questionData.reason, button);
            }
        });
        optionsContainer.appendChild(button);
    });
}

// ฟังก์ชันสำหรับตรวจคำตอบ
function checkAnswer(selectedIndex, correctAnswerIndex, reason, clickedButton) {
    const optionsContainer = document.getElementById('optionsContainer');
    optionsContainer.style.pointerEvents = 'none'; // ปิดการคลิกตัวเลือกอื่น

    // ลบ class correct/wrong ออกจากปุ่มทั้งหมดก่อน
    Array.from(optionsContainer.children).forEach(button => {
        button.classList.remove('correct', 'wrong');
    });

    const feedbackTextElement = document.getElementById('feedbackText');
    const explanationTextElement = document.getElementById('explanationText');
    const feedbackContainer = document.getElementById('feedbackContainer');

    feedbackTextElement.classList.remove('correct-feedback', 'wrong-feedback'); // ลบ class เก่าออก

    if (selectedIndex === correctAnswerIndex) {
        score++;
        clickedButton.classList.add('correct'); // เพิ่ม class 'correct' ให้ปุ่มที่ตอบถูก
        feedbackTextElement.innerText = "ถูกต้อง!";
        feedbackTextElement.classList.add('correct-feedback');
    } else {
        clickedButton.classList.add('wrong'); // เพิ่ม class 'wrong' ให้ปุ่มที่ตอบผิด
        // แสดงคำตอบที่ถูกต้อง
        Array.from(optionsContainer.children).forEach((button, i) => {
            if (i === correctAnswerIndex) {
                button.classList.add('correct');
            }
        });
        // ลบตัวเลขนำหน้าออกจากข้อความคำถามใน feedbackTextElement
        const cleanQuestionTextForFeedback = questions[currentQuestionIndex].question.replace(/^\d+\.\s*/, '');
        feedbackTextElement.innerText = "ผิด! คำตอบที่ถูกต้องคือ " + cleanQuestionTextForFeedback;
        feedbackTextElement.classList.add('wrong-feedback');
    }

    explanationTextElement.innerText = reason; // แสดงเหตุผล
    feedbackContainer.style.display = 'block'; // แสดงส่วน feedback

    document.getElementById('nextButton').style.display = 'block'; // แสดงปุ่ม "ข้อต่อไป"
}

// ฟังก์ชันสำหรับแสดงผลคะแนน
function displayResult() {
    quizCompleted = true;
    document.getElementById('quizScreen').style.display = 'none';
    document.getElementById('resultScreen').style.display = 'block';
    document.getElementById('scoreDisplay').innerText = `คุณได้คะแนน ${score} จาก ${questions.length} ข้อ`;
}

// ฟังก์ชันสำหรับเริ่มทำข้อสอบใหม่
function restartQuiz() {
    currentQuestionIndex = 0;
    score = 0;
    quizCompleted = false;
    selectedOptionButton = null;
    document.getElementById('resultScreen').style.display = 'none';
    document.getElementById('homeScreen').style.display = 'block'; // กลับไปหน้าจอหลัก
    closeNav(); // ปิด sidebar เมื่อกลับหน้าหลัก
}

