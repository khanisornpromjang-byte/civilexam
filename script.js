// ฟังก์ชันสำหรับลบคำถามซ้ำ
// จะตรวจสอบจากคุณสมบัติ 'question' เพื่อหาคำถามที่ซ้ำกัน
function removeDuplicateQuestions() {
    const uniqueQuestions = [];
    const seenQuestions = new Set(); // ใช้ Set ในการเก็บข้อความคำถามที่เคยเห็นแล้ว

    questions.forEach(q => {
        // ลบตัวเลขนำหน้าและจุดออกจากข้อความคำถามก่อนตรวจสอบความซ้ำ
        const cleanQuestion = q.question.replace(/^\d+\.\s*/, '');
        if (!seenQuestions.has(cleanQuestion)) {
            seenQuestions.add(cleanQuestion);
            // เพิ่มคำถามต้นฉบับ (ที่มีตัวเลขนำหน้า) เข้าไปใน uniqueQuestions
            uniqueQuestions.push(q);
        }
    });

    // ล้าง array questions เดิม แล้วใส่คำถามที่ไม่ซ้ำเข้าไป
    questions.length = 0; // ล้าง array เดิม
    questions.push(...uniqueQuestions); // เพิ่มคำถามที่ไม่ซ้ำเข้าไป

    console.log(`จำนวนคำถามทั้งหมดหลังลบคำถามซ้ำ: ${questions.length} ข้อ`);
}

